#include <libftprintf.h>

long long   bin(int n)
{
    long long bn;
    int i;
    int j;

    bn = 0;
    i = 1;
    j = 1;
    while( n != 0)
    {
        j = n % 2;
        n /= 2;
        bn += j * i;
        i *= 10;
    }
    return(bn);
}